-- 2) Expedientes legales — header de cada caso.

CREATE TABLE IF NOT EXISTS public.legal_flow_expedientes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  id_cuenta_cobranza BIGINT NOT NULL REFERENCES public.cuentas_cobranza(id),
  -- Tipo de solicitud (match con frontend RequestType en legal-flow.ts):
  -- Nuevo contrato | Nuevo convenio | Modificatorio | Renovación | Terminación | Validación externa
  tipo_solicitud text NOT NULL DEFAULT 'Nuevo contrato',
  etapa text NOT NULL DEFAULT 'En revisión legal'
    REFERENCES public.legal_flow_etapas(codigo),
  prioridad text NOT NULL DEFAULT 'Medio' CHECK (prioridad IN ('Alto','Medio','Bajo')),
  titulo text,
  descripcion text,
  -- Asignación + autoría: guardamos email para evitar acoplamiento fuerte
  -- contra usuarios; si se quiere FK estricta cambiar a
  -- id_usuario_asignado INTEGER REFERENCES usuarios(id).
  email_asignado text,
  email_solicitante text NOT NULL,
  -- Template usado (opcional, FK suave a tabla de templates si se crea luego).
  id_template integer,
  -- Fechas del proceso
  fecha_limite timestamptz,
  fecha_aprobacion timestamptz,
  fecha_envio_firma timestamptz,
  fecha_firmado timestamptz,
  notas_rechazo text,
  -- Campos de control
  activo boolean NOT NULL DEFAULT true,
  fecha_creacion timestamptz NOT NULL DEFAULT now(),
  fecha_actualizacion timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS legal_flow_expedientes_cuenta_idx
  ON public.legal_flow_expedientes (id_cuenta_cobranza);
CREATE INDEX IF NOT EXISTS legal_flow_expedientes_etapa_idx
  ON public.legal_flow_expedientes (etapa) WHERE activo;
CREATE INDEX IF NOT EXISTS legal_flow_expedientes_asignado_idx
  ON public.legal_flow_expedientes (email_asignado) WHERE activo;


-- 3) Histórico de transiciones — audit log.

CREATE TABLE IF NOT EXISTS public.legal_flow_historico (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  id_expediente bigint NOT NULL REFERENCES public.legal_flow_expedientes(id) ON DELETE CASCADE,
  etapa_anterior text REFERENCES public.legal_flow_etapas(codigo),
  etapa_nueva text NOT NULL REFERENCES public.legal_flow_etapas(codigo),
  fecha timestamptz NOT NULL DEFAULT now(),
  email_usuario text NOT NULL,
  notas text,
  -- Campos de control
  activo boolean NOT NULL DEFAULT true,
  fecha_creacion timestamptz NOT NULL DEFAULT now(),
  fecha_actualizacion timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS legal_flow_historico_expediente_idx
  ON public.legal_flow_historico (id_expediente, fecha DESC);

DROP TRIGGER IF EXISTS legal_flow_historico_set_fecha_actualizacion ON public.legal_flow_historico;
CREATE TRIGGER legal_flow_historico_set_fecha_actualizacion
  BEFORE UPDATE ON public.legal_flow_historico
  FOR EACH ROW EXECUTE FUNCTION public.legal_flow_actualizar_fecha_modificacion();


-- 4) Trigger de bitácora: cada INSERT o cambio de `etapa` en un expediente
--    registra una fila en el histórico.

CREATE OR REPLACE FUNCTION public.legal_flow_registrar_cambio_etapa()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    INSERT INTO public.legal_flow_historico (
      id_expediente, etapa_anterior, etapa_nueva, email_usuario
    ) VALUES (
      NEW.id, NULL, NEW.etapa, COALESCE(NEW.email_asignado, NEW.email_solicitante)
    );
    RETURN NEW;
  ELSIF OLD.etapa IS DISTINCT FROM NEW.etapa THEN
    INSERT INTO public.legal_flow_historico (
      id_expediente, etapa_anterior, etapa_nueva, email_usuario
    ) VALUES (
      NEW.id, OLD.etapa, NEW.etapa, COALESCE(NEW.email_asignado, NEW.email_solicitante)
    );
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS legal_flow_expedientes_etapa_change ON public.legal_flow_expedientes;
CREATE TRIGGER legal_flow_expedientes_etapa_change
  AFTER INSERT OR UPDATE OF etapa ON public.legal_flow_expedientes
  FOR EACH ROW EXECUTE FUNCTION public.legal_flow_registrar_cambio_etapa();

-- Refresca fecha_actualizacion en cualquier UPDATE del expediente.
DROP TRIGGER IF EXISTS legal_flow_expedientes_set_fecha_actualizacion ON public.legal_flow_expedientes;
CREATE TRIGGER legal_flow_expedientes_set_fecha_actualizacion
  BEFORE UPDATE ON public.legal_flow_expedientes
  FOR EACH ROW EXECUTE FUNCTION public.legal_flow_actualizar_fecha_modificacion();
