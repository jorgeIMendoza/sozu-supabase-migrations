-- SOZU Legal Flow — esquema base para el Pipeline de Contratos
--
-- Conecta las etapas 2..6 del "Panel de Operaciones" de SOZU Legal Flow a
-- datos reales. La etapa 1 ("Solicitud recibida") sigue derivándose de
-- cuentas_cobranza + propiedades.id_estatus_disponibilidad = 4 (Apartado);
-- no existe row aquí hasta que el operador da "Revisar →".
--
-- Relación: una cuenta puede tener varios expedientes (contrato original,
-- modificatorios, prórrogas, terminación) ⇒ legal_flow_expedientes es 1:N
-- contra cuentas_cobranza.
--
-- Ref spec: Ejecuciones/ejecutar.md (Ramón, 2026-05-30).
-- Notas de diseño:
--   * cuentas_cobranza.id es bigint en el baseline ⇒ la FK usa bigint
--     (el .md decía integer — corregido para evitar mismatch de tipos).
--   * Todos los valores de datos van en español ("Primera mayúscula con
--     espacios"). Los códigos enum del frontend (src/types/legal-flow.ts:
--     CaseStatus, RequestType, Priority) deben actualizarse para coincidir.
--   * Las 3 tablas llevan campos de control (activo, fecha_creacion,
--     fecha_actualizacion). fecha_actualizacion se refresca en cada UPDATE
--     vía trigger genérico.

-- 0) Función genérica: refresca fecha_actualizacion en cada UPDATE.

CREATE OR REPLACE FUNCTION public.legal_flow_actualizar_fecha_modificacion()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.fecha_actualizacion := now();
  RETURN NEW;
END;
$$;


-- 1) Catálogo de etapas. El codigo (PK) es el valor en español que también
--    usa el frontend como enum CaseStatus. Mantener sincronizado.

CREATE TABLE IF NOT EXISTS public.legal_flow_etapas (
  codigo text PRIMARY KEY,
  nombre text NOT NULL,
  orden  smallint NOT NULL,
  es_terminal boolean NOT NULL DEFAULT false,
  activo boolean NOT NULL DEFAULT true,
  fecha_creacion timestamptz NOT NULL DEFAULT now(),
  fecha_actualizacion timestamptz NOT NULL DEFAULT now()
);

