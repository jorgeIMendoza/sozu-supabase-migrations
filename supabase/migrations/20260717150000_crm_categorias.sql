-- Categorías de contacto del CRM (procedencia). Catálogo PROPIO del CRM para NO
-- mezclar con `tipos_entidad` (estructural del sistema, usado en muchas partes).
-- Un contacto (= fila de `entidades_relacionadas`, prospecto tipo 7 / comprador
-- tipo 2) puede tener VARIAS categorías -> tabla puente M:N.

-- ─── Catálogo crm_categorias ──────────────────────────────────────────────────
CREATE TABLE public.crm_categorias (
    id              INTEGER      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre          TEXT         NOT NULL UNIQUE,
    descripcion     TEXT,
    orden           INTEGER      NOT NULL DEFAULT 100,
    activo          BOOLEAN      NOT NULL DEFAULT TRUE,
    fecha_creacion  TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);

