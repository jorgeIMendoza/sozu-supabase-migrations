-- CRUD de estados de lead del CRM. Hasta ahora los estados eran un array fijo en el
-- front (META_LEAD_STATUSES). Se vuelven configurables: catálogo en BD con color y
-- orden editables. `crm_leads_atribucion.estatus_lead` (TEXT) sigue guardando la `clave`.
-- Idempotente y self-guarded. El submenú se crea resolviendo el menú CRM ACTIVO desde un
-- submenú hermano existente (robusto dev/prod, sin ids fijos). Los ROLES que ven la
-- pantalla se asignan desde Roles y Permisos (UI), NO aquí.

-- ─── Catálogo crm_estados_lead ────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.crm_estados_lead (
    id                   INTEGER      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    clave                TEXT         NOT NULL UNIQUE,            -- valor en crm_leads_atribucion.estatus_lead
    nombre               TEXT         NOT NULL,                   -- etiqueta visible
    color                TEXT         NOT NULL DEFAULT '#64748b', -- hex para el badge
    orden                INTEGER      NOT NULL DEFAULT 100,
    activo               BOOLEAN      NOT NULL DEFAULT TRUE,
    fecha_creacion       TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    fecha_actualizacion  TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_crm_estados_lead_orden ON public.crm_estados_lead (orden);

-- Trigger fecha_actualizacion (reusa la función compartida de 20260721080000).
DROP TRIGGER IF EXISTS trg_crm_estados_lead_fecha_actualizacion ON public.crm_estados_lead;
CREATE TRIGGER trg_crm_estados_lead_fecha_actualizacion
BEFORE UPDATE ON public.crm_estados_lead
FOR EACH ROW EXECUTE FUNCTION public.crm_set_fecha_actualizacion();

-- ─── RLS (espejo de crm_pipelines) ────────────────────────────────────────────
ALTER TABLE public.crm_estados_lead ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "crm_estados_lead_select" ON public.crm_estados_lead;
CREATE POLICY "crm_estados_lead_select" ON public.crm_estados_lead
    FOR SELECT TO authenticated USING (true);

DROP POLICY IF EXISTS "crm_estados_lead_insert" ON public.crm_estados_lead;
CREATE POLICY "crm_estados_lead_insert" ON public.crm_estados_lead
    FOR INSERT TO authenticated WITH CHECK (true);

DROP POLICY IF EXISTS "crm_estados_lead_update" ON public.crm_estados_lead;
CREATE POLICY "crm_estados_lead_update" ON public.crm_estados_lead
    FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

DROP POLICY IF EXISTS "crm_estados_lead_delete" ON public.crm_estados_lead;
CREATE POLICY "crm_estados_lead_delete" ON public.crm_estados_lead
    FOR DELETE TO authenticated USING (is_admin_user());

