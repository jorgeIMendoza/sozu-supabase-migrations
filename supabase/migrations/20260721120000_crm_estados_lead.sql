-- ─── Submenú Configuración › Estados de lead ──────────────────────────────────
-- menu_id se deriva del submenú hermano "Pipelines" (misma sección, menú activo) para
-- no hardcodear ids que difieren entre dev y prod. IDENTITY asigna el id del submenú.
INSERT INTO public.submenus (menu_id, nombre, vista_front_end, orden, activo, solo_usuarioa)
SELECT sib.menu_id, 'Estados de lead', '/admin/portal-crm/configuracion/estados-lead', 682, true, false
FROM (
    SELECT s2.menu_id
    FROM public.submenus s2
    JOIN public.menus m2 ON m2.id = s2.menu_id
    WHERE s2.vista_front_end = '/admin/portal-crm/configuracion/pipelines'
      AND s2.activo = true AND m2.activo = true
    LIMIT 1
) sib
WHERE NOT EXISTS (
    SELECT 1 FROM public.submenus s
    WHERE s.vista_front_end = '/admin/portal-crm/configuracion/estados-lead'
      AND s.menu_id = sib.menu_id);

-- Permisos DISPONIBLES del submenú (catálogo de acciones: leer/crear/actualizar/eliminar).
INSERT INTO public.submenus_permisos_disponibles (submenu_id, permiso_id, activo)
SELECT s.id, p.permiso_id, true
FROM public.submenus s
JOIN public.menus m ON m.id = s.menu_id
CROSS JOIN (VALUES (1), (2), (3), (4)) AS p(permiso_id)
WHERE s.vista_front_end = '/admin/portal-crm/configuracion/estados-lead'
  AND s.activo = true AND m.activo = true
  AND NOT EXISTS (
      SELECT 1 FROM public.submenus_permisos_disponibles d
      WHERE d.submenu_id = s.id AND d.permiso_id = p.permiso_id);
